# Python program to enumerate basic named Python data-types with examples

# Integer
integer_example = 42
print(f"Integer: {integer_example}")

# Float
float_example = 3.14159
print(f"Float: {float_example}")

# Complex
complex_example = 1 + 2j
print(f"Complex: {complex_example}")

# String
string_example = "Hello, world!"
print(f"String: {string_example}")

# Boolean
boolean_example = True
print(f"Boolean: {boolean_example}")

# List
list_example = [1, 2, 3, 4, 5]
print(f"List: {list_example}")

# Tuple
tuple_example = (1, 2, 3, 4, 5)
print(f"Tuple: {tuple_example}")

# Set -- Note that adding an element multiple times
# does not add copies
set_example = {1, 2, 3, 4, 5, 2, 2}
print(f"Set: {set_example}")

# Dictionary
dictionary_example = {"one": 1, "two": 2, "three": 3}
print(f"Dictionary: {dictionary_example}")

# NoneType
none_example = None
print(f"NoneType: {none_example}")
